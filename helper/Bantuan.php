// Function to validate if the input is an integer
function validateIntegerInput($input)
{
    return filter_var($input, FILTER_VALIDATE_INT) !== false;
}

// Function to get an integer input from the user
function getInputInteger($prompt)
{
    $isValid = false;
    do {
        $userInput = readline($prompt);
        $isValid = validateIntegerInput($userInput);
        if (!$isValid) {
            echo "INVALID INPUT. TOLONG MASUKKAN DENGAN INTEGER" . PHP_EOL;
        }
    } while (!$isValid);

    return (int) $userInput;
}
